/**=======================================================
 * 全局静态类, 用于声明一个新的类并提供构造函数支持
 * 
 * ======================================================*/
var Clazz = {
	/**
	 * 返回一个函数, 即新声明的类的构造函数
	 */
	create : function() {
		// 命名为initialize的函数将被这个类实现作为类的构造函数.
		return function() {
			// initialize函数将在实例化一个变量的时候被调用执行.
			this.initialize.apply(this, arguments);
		};
	}
};